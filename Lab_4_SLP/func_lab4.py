def find_a_file():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    file_path = os.path.join(script_dir, "Втеча з Шоушенка.txt")

    if not os.path.exists(file_path):
        print(f"Помилка: Файл не знайдено за шляхом {file_path}")

    return file_path

def len_text_with_spaces():
    """
    Повертає кількість символів у тексті 
    з пробілами з файлу Втеча з Шоушенка.txt
    """
    file_path = find_a_file()
    
    if os.path.exists(file_path):
        with open(file_path, 'r', encoding='utf-8') as file:
            content = file.read()
        return len(content)
    return 0

def len_text_without_spaces():
    """
    Повертає кількість символів у тексті 
    без пробілів з файлу Втеча з Шоушенка.txt
    """
    file_path = find_a_file()
    
    if os.path.exists(file_path):
        with open(file_path, 'r', encoding='utf-8') as file:
            content = file.read()
        return len(content.replace(" ", ""))
