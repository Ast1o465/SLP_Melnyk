def total_number_of_different_words():
    """
    загальна кількість різних слів (без повторів).  
    """
    file_path = find_a_file()

    if os.path.exists(file_path):
        with open(file_path, 'r', encoding='utf-8') as file:
            content = file.read()
        words = content.replace(".", " ").replace(",", " ").replace("-", " ").split()
        return len(set(words))
    return 0
    
def words_one():
    """
    знаходить кількість слів які зустрічаються тільки 1 раз у тексті
    """
    file_path = find_a_file()

    if os.path.exists(file_path):
        with open(file_path, 'r', encoding='utf-8') as file:
            content = file.read()
        words = content.replace(".", " ").replace(",", " ").replace("-", " ").split()
        word_counts = {word: words.count(word) for word in set(words)}
        return sum(1 for count in word_counts.values() if count == 1)
    return 0
    
