def random_word(words):
    """Повертає випадкове слово з списку слів"""
    import random
    return random.choice(words)

def random_sentence(nouns, verbs, adjectives):
    """Повертає випадкове речення з іменниками, дієсловами та прикметниками"""
    import random
    sentence = f"{random.choice(adjectives).capitalize()} {random.choice(nouns)} {random.choice(verbs)}."
    return sentence



def create_csv_file(output_dir, filename, words):
    """Створює CSV-файл з вказаною назвою та списком слів"""
    filepath = os.path.join(output_dir, filename)
    with open(filepath, 'w', newline='', encoding='utf-8') as file:
        writer = csv.writer(file)
        writer.writerow(["номер", "слово"])  # Заголовок
        for i, word in enumerate(words, 1):
            writer.writerow([i, word])
    print(f"Файл {filename} створено успішно!")


